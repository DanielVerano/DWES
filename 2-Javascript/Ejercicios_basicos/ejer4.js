const num1 = parseFloat(prompt('Escriba el primer número', 0));
const num2 = parseFloat(prompt('Escriba el segundo número', 0));
const num3 = parseFloat(prompt('Escriba el tercer número', 0));
const num4 = parseFloat(prompt('Escriba el cuarto número', 0));

alert(`La suma de los cuatro es: ${num1 + num2 + num3 + num4}`);
alert(`El producto de los cuatro es: ${num1 * num2 * num3 * num4}`);
alert(`La resta de los dos primeros es: ${num1 - num2}`);
alert(`La división de los dos últimos es: ${num3 / num4}`);